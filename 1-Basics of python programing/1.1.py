#Program to display different data type and constants
#String
name = "John snow"
print("name is : ",name)
print("type of name is : ",type(name))
#Integer
age = 30    
print("age is : ",age)
print("type of age is : ",type(age))
#Float      
height = 5.9
print("height is : ",height)
print("type of height is : ",type(height))
#Boolean
is_student = False
print("is student : ",is_student)
print("type of is_student is : ",type(is_student))
#list
fruits = ["apple", "banana", "cherry"]
print("fruits are : ",fruits)   
print("type of fruits is : ",type(fruits))
